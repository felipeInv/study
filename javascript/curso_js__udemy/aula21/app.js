/*
    Operadores de comparação

    > maior que
    >= menor ou igual que
    < menor que
    <= menor ou igual que
    == igual (valor)
    === igual restritamente (valor e tipo) (se uma delas for verdadeira, retorna true)
    != diferente (valor)
    !== diferente restritamente (valor e tipo) (se uma delas for verdadeira, retorna true)
*/
console.log(10>5); //true
console.log(10>=5); //true
console.log(10<5);  //false
console.log(10<=5); // false
console.log(10==5); //false
console.log(10==='5');  //false
console.log(10!=5); //true
console.log(10!=='5'); //true

